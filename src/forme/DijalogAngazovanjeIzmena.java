    private void popuniPredmete() {
        Zahtev zahtev = new Zahtev(Operacija.VRATI_PREDMETE, null);
        
        Komunikacija.getKomunikacija().posaljiZahtev(zahtev);
        
        Odgovor odg = Komunikacija.getKomunikacija().primiOdgovor();
        
        ArrayList<Predmet> lista = (ArrayList<Predmet>) odg.getOdgovor();
        
        cmbPredmeti.removeAllItems();
        
        for (Predmet predmet : lista) {
            cmbPredmeti.addItem(predmet);
        }
    }


    void srediFormuZaIzmenu(Angazovanje a) {
        btnDodaj.setEnabled(false);
        btnIzmena.setEnabled(true);
        this.a = a;
        
        cmbPredmeti.setVisible(false);
        labelaPredmet.setVisible(false);
        SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");
        txtDatum.setText(sdf.format(a.getDatum()));
    }

   
