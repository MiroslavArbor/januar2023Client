    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed
        SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");
        
        Predmet predmet = (Predmet) cmbPredmeti.getSelectedItem();
        Date datum = null;
        
        try {
            datum = sdf.parse(txtDatum.getText());
        } catch (ParseException ex) {
            Logger.getLogger(DijalogAngazovanjeIzmena.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Datum mora biti u formatu dd.MM.yyyy");
            return;
        }
        
        GlavnaForma gf = (GlavnaForma) getParent();
        
        gf.unesiNovoAngazovanjeZaIzmenu(predmet, p, datum);
    }//GEN-LAST:event_btnDodajActionPerformed

